    boolean camposNoVacios()
    {   boolean vacios=false;
        if(jTextField1.getText().length()==0)
        {   JOptionPane.showMessageDialog(this, "Porfavor Ingrese ID");
            jTextField1.requestFocus();
            vacios=true;
        }
        else if(jTextField2.getText().length()==0)
        {   JOptionPane.showMessageDialog(this, "Porfavor Ingrese Puesto");
            jTextField2.requestFocus();
            vacios=true;
        }
        return vacios;
    }
    
    void registrar()
    {   try
        {   st=con.createStatement();
            st.executeUpdate("INSERT INTO Puesto VALUES ('"+jTextField1.getText()+"','"+
                    jTextField2.getText()+"';");
            jTextField1.setText("");
            jTextField2.setText("");
            JOptionPane.showMessageDialog(this, "Puesto Registrado !");
            dispose();
        }
        catch(SQLException e){  JOptionPane.showMessageDialog(this, "Error Debido a : "+e.toString());}
    }
        
