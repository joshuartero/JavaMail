    String correoTrabajador(String nombresApellidos)
    {   String correo="";
        try
        {   st = (Statement) con.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE, ResultSet.CONCUR_READ_ONLY);
            rs2=st.executeQuery("SELECT T.Correo FROM Trabajador T INNER JOIN Persona P ON T.`DNI`=P.`DNI`WHERE "
                + "CONCAT(P.`APELLIDOP`, ' ',P.`APELLIDOM`, ' ',P.`NOMBRES`)='"+nombresApellidos+"';");
            if(rs2.next())
               correo=rs2.getString(1);                            
        }
        catch(SQLException e){  JOptionPane.showMessageDialog(this, "Error Debido a : "+e.toString());}
        return correo;  
    }    
    
