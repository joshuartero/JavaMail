    void listarArticulos()
    {   modelo=(DefaultTableModel) jTable1.getModel();
        try
        {   st=con.createStatement();
            rs=st.executeQuery("SELECT NS.`CANTIDAD`, A.`DESCRIPCION` FROM Nota_Salida NS INNER JOIN Articulo A ON NS.`CODARTICULO`= A.`CODIGO` WHERE NS.Codigo='"+jTextField1.getText()+"'");
            while(rs.next())
            {   Object rowData[]={rs.getString(1), rs.getString(2)};
                modelo.addRow(rowData);
            }
        }
        catch(SQLException e){JOptionPane.showMessageDialog(this, "ERROR DEBIDO A : "+e.toString());}    
    }
    
