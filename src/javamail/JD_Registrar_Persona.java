    boolean camposNoVacios()
    {   boolean vacios=false;
        if(jTextField1.getText().length()==0)
        {   JOptionPane.showMessageDialog(this, "Porfavor Ingrese D.N.I.");
            jTextField1.requestFocus();
            vacios=true;
        }
        else if(jTextField2.getText().length()==0)
        {   JOptionPane.showMessageDialog(this, "Porfavor Ingrese Nombres");
            jTextField2.requestFocus();
            vacios=true;
        }
        else if(jTextField3.getText().length()==0)
        {   JOptionPane.showMessageDialog(this, "Porfavor Ingrese Apellido Paterno");
            jTextField3.requestFocus();
            vacios=true;
        }
        else if(jTextField4.getText().length()==0)
        {   JOptionPane.showMessageDialog(this, "Porfavor Ingrese Apellido Materno");
            jTextField4.requestFocus();
            vacios=true;
        }
        return vacios;
    }
    
    void registrar()
    {   try
        {   st=con.createStatement();
            st.executeUpdate("INSERT INTO Persona VALUES ('"+jTextField1.getText()+"','"+
                    jTextField2.getText()+"','"+jTextField3.getText()+"','"+jTextField4.getText()+"','"+capturarFecha()+"');");
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            dateChooserCombo1.setCurrent(null);
            JOptionPane.showMessageDialog(this, "Persona Registrada !");
        }
        catch(SQLException e){  JOptionPane.showMessageDialog(this, "Error Debido a : "+e.toString());}
    }
    
    String capturarFecha()
    {   int año = dateChooserCombo1.getCurrent().get(Calendar.YEAR);
        int mes = dateChooserCombo1.getCurrent().get(Calendar.MONTH) + 1;
        int dia = dateChooserCombo1.getCurrent().get(Calendar.DAY_OF_MONTH);
        return año+"-"+mes+"-"+dia;
    }
    
